def graham_schmidt(B:np.ndarray)->np.ndarray:
    """Return an orthogonal basis from a given B basis

    Args:
        B (np.ndarray): B basis
    """
   
    B_ortho = []
    for i in range(len(B)):
        e_i = B[i]
        for j in range (len(B_ortho)):
            e_j = B_ortho[j]
            p_j = np.linalg.vecdot(e_i, e_j)*e_j/np.linalg.vecdot(e_j, e_j)
            e_i = e_i - p_j
        B_ortho.append(e_i)
    return(B_ortho)


def regression_projection_vectorielle(X:np.ndarray, Y:np.ndarray, d:int=1):
    """Performs polynomial regression by projecting Y vector onto the regression subs-space.
        Solves the system by reframming it triangularly

    Args:
        X (np.ndarray): n samples of real values
        Y (np.ndarray): n samples of real values
        d (int, optional): Fitting polynome degree. Defaults to 1.
    """

    A = np.array([[xi**(d-i) for i in range(d+1)] for xi in X])

    col_A = np.array([A[:, i] for i in range(d+1)])
    
    # Graham-Schmidt :
    col_A_orthogonal = graham_schmidt(col_A)
    col_A_orthonormal = [e/np.linalg.norm(e) for e in col_A_orthogonal]
    
    Y_estimate = np.sum(np.array([np.linalg.vecdot(Y, e)*e for e in col_A_orthonormal]), axis = 0)

    # Matrice triangulaire et base orthogonale
    Q = np.column_stack(col_A_orthonormal)
    R = Q.T @ A # A = QR <=> R = Q^T.A
    QT_Y_estimate = Q.T @ Y_estimate

    # Q^T.Y_estimate = R.w
    w = np.zeros(len(col_A))
    for i in range(len(col_A)-1, -1, -1):
        w[i] = (QT_Y_estimate[i] - np.dot(R[i, i+1:], w[i+1:])) / R[i, i]

    return(w)



